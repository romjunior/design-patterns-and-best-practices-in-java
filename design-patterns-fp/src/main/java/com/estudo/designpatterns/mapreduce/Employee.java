package com.estudo.designpatterns.mapreduce;

public class Employee {

    private final int id;
    private final String name;
    private final int age;

    public Employee(int id, String name, int age) {
        this.id = id;
        this.name = name;
        this.age = age;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }
}
